    // Purchase table data fetching
    private ArrayList<Record> fetchData() throws IllegalAccessException
    {
        ArrayList<Record> data = new ArrayList<>();
        
        String query = "SELECT * FROM PURCHASE ORDER BY DATE DESC";
        Statement st;
        try {
            DatabaseConnect db = new DatabaseConnect();
    
            st = db.conn.createStatement();
            ResultSet rs =  st.executeQuery(query);
            Record rec;
            while(rs.next())
            {
                rec = new Record(rs.getString("PID"), rs.getTimestamp("DATE"), rs.getInt("QUANTITY"), rs.getInt("RATE"), rs.getInt("TOTAL"));
                data.add(rec);
            }
            st.close();
            
        } catch (SQLException ex) {
            System.out.println("Error in Fetching data");
            Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException | InstantiationException ex) {
            Logger.getLogger(Purchase.class.getName()).log(Level.SEVERE, null, ex);
        }
        return data;
        
    }
    
    private void DisplayData() throws IllegalAccessException
    {
        ArrayList<Record> data = fetchData();
        DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
        Object[] row = new Object[5];
        
        for(int i=0; i<data.size(); i++)
        {
            row[0] = data.get(i).getDate();
            row[1] = data.get(i).getPid();
            row[2] = data.get(i).getQuant();
            row[3] = data.get(i).getRate();
            row[4] = data.get(i).getTotal();
            model.addRow(row);
        }
        tblHeader();
    }
