    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
        
        if(tblDisplay.getSelectedRowCount() == 1)
        {
            int row = tblDisplay.getSelectedRow();
            String sql = "DELETE FROM PURCHASE WHERE PID=?";
            try {
                
                DatabaseConnect db = new DatabaseConnect();
                PreparedStatement pst = db.conn.prepareStatement(sql);
                pst.setString(1, (String) model.getValueAt(row, 1));
                pst.execute();
                model.removeRow(row);
                JOptionPane.showMessageDialog(null, "Deleted Successfully");
                pst.close();
                
            } catch (SQLException ex) {
                Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Something went wrong!");
            } catch (ClassNotFoundException | InstantiationException | IllegalAccessException ex) {
                Logger.getLogger(Purchase.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        else
        {
            if(tblDisplay.getRowCount() == 0)
            {
                JOptionPane.showMessageDialog(null, "No Record is Present!");
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Please select a single row!");
               
            }
        }    
    }//GEN-LAST:event_btnDeleteActionPerformed
    
    private String GenerateStamp()
    {
        Date dt = new Date();
        String stamp = new SimpleDateFormat("yyyyMMddHHmmss").format(new Timestamp(dt.getTime()));
        return stamp;
    }
